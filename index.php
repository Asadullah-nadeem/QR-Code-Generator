  <script>
    function generateQRCode() {
      var userText = document.getElementById("qrInput").value.trim();
      if (userText !== "") {
        var qrImage = document.getElementById("qrImage");
        qrImage.src = "https://api.qrserver.com/v1/create-qr-code/?size=150x150&data=" + encodeURIComponent(userText);
      } else {
        alert("Please enter text to generate QR Code.");
      }
    }
  </script>
